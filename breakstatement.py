#latihan break statement
#break statement digunakan untuk menghentikan perulangan secara tiba-tiba saat kondisi tertentu sudah terpenuhi

j = 0
while j < 8:
    if j == 6:
        break #menghentikan seluruh pengulangan. Jika j = 6 maka output akan mengeluarkan nilai 0-5 dan angka 6 kena break
    print(j)
    j += 1
    
print("akhir break")
    
    
  
